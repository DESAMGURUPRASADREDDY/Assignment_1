s="I am a teacher and I love to inspire and teach people"     # Given String
sep=s.split()                                                 # To split given string
print(sep)                                                    # To print string after splitting
b=set(sep)                                                    # To make set of string to remove duplicates
print(b)                                                      # To print set
print(len(b))                                                 # To print unique words of set